#!/bin/bash

# =======================================================================
# 파일명: monitor.sh
# 목적: 애플리케이션 상태 감시 및 자원(CPU, Memory, Disk) 관제 자동화
# 소유 및 권한: 소유자 agent-dev, 그룹 agent-core, 권한 750 (명세서 규격 준수)
# 실행 방식: 크론탭(crontab)에 의해 agent-admin 계정으로 매분 자동 실행
# =======================================================================

# -----------------------------------------------------------------------
# 0. 글로벌 환경 설정 및 타깃 지정
# -----------------------------------------------------------------------
# 관제 로그가 누적 저장될 절대 경로입니다.
LOG_FILE="/var/log/agent-app/monitor.log"

# 표준 규격 포맷인 [YYYY-MM-DD HH:MM:SS] 형태로 현재 시간을 상수에 저장합니다.
TIMESTAMP=$(date "+%Y-%m-%d %H:%M:%S")

# [B1-2 트러블슈팅 핵심 수정] 
# B1-2 과제의 타깃 애플리케이션 바이너리 이름인 'agent-leak-app-x86'으로 정정합니다.
PROCESS_NAME="agent-leak-app-x86"

# 에이전트가 통신을 위해 리슨(LISTEN)하고 있어야 하는 내부 고정 포트입니다.
PORT="15034"


# ==================================================================
# STEP 1. 프로세스 생존 확인 (Health Check)
# ==================================================================
# pgrep -f 옵션을 사용하여 프로세스 명령어 전체 라인에서 키워드를 검색합니다.
# -o 옵션은 메모리 누수로 인해 혹시 여러 프로세스가 떴을 경우 가장 먼저 실행된 본체의 PID를 잡습니다.
PID=$(pgrep -fo "$PROCESS_NAME")

# 만약 PID 변수가 텅 비어있다면(-z) 어플리케이션이 OOM 등으로 죽은 상태입니다.
if [ -z "$PID" ]; then
    echo "[ERROR] Process '$PROCESS_NAME' is not running. Terminating monitor."
    exit 1 # 명세서에 따라 즉시 스크립트를 에러 종료(exit 1) 시킵니다.
fi


# ==================================================================
# STEP 2. 네트워크 포트 바인딩 확인
# ==================================================================
# ss -tuln 명령어로 현재 열려있는 TCP(-t) 및 UDP(-u) 소켓 리스트를 숫자로 확인합니다.
# grep -q 옵션은 화면에 텍스트를 출력하지 않고 매칭되는 결과가 있는지만 조용히 탐색합니다.
if ! ss -tuln | grep -q ":$PORT "; then
    echo "[ERROR] Port $PORT is not in LISTEN state. Terminating monitor."
    exit 1 # 포트가 닫혀있다면 정상 서비스 상태가 아니므로 즉시 에러 종료합니다.
fi


# ==================================================================
# STEP 3. 방화벽(UFW) 상태 실시간 점검
# ==================================================================
# 방화벽이 꺼진 것은 보안 경고 대상이지만, 앱 자체가 죽은 것은 아니므로
# [WARNING] 문구만 출력할 뿐 스크립트를 종료하지 않고 다음 자원 수집 단계로 넘어갑니다.
if ! sudo ufw status | grep -q "Status: active"; then
    echo "[WARNING] UFW firewall is currently INACTIVE!"
fi


# ==================================================================
# STEP 4. 시스템 물리 자원 수집 (Metric Collection)
# ==================================================================

# 1) CPU 사용률 (%) 계산
# top을 2회 측정(-bn2)한 뒤 tail로 최신값만 추출하여 현재 CPU 상태를 정확히 반영합니다.
# (1회 측정은 부팅 이후 누적 평균이라 부정확하므로 2회 측정의 두 번째 값을 사용)
# sed로 'id'(유휴) 앞 숫자를 환경에 상관없이 안정적으로 뽑아내고,
# 전체 100%에서 유휴값을 빼 실제 사용률을 소수점 첫째 자리까지 구합니다.
# CPU=$(top -bn1 | awk '/Cpu\(s\)/ {printf "%.1f", 100 - $8}')   # (구버전: 위치 기반, 환경 취약)
CPU=$(top -bn2 | grep "Cpu(s)" | tail -n 1 | sed 's/.*, *\([0-9.]*\)%* id.*/\1/' | awk '{printf "%.1f", 100 - $1}')

# 2) 메모리 사용률 (%) 계산
# free 명령어의 결과를 받아 '사용 중인 메모리($3) / 전체 메모리($2) * 100' 공식을 사용해
# 현재 가상머신 내부의 메모리 점유율을 실시간 계산합니다. B1-2 누수 분석의 핵심 데이터가 됩니다.
MEM=$(free | awk '/Mem:/ {printf "%.1f", $3/$2 * 100}')

# 3) 루트(/) 디스크 파싱 (Used %)
# df 명령어의 오작동을 막기 위해 '--output=pcent' 옵션으로 퍼센트 열만 콕 집어 출력한 뒤,
# tail 명령어와 tr 명령어를 조합해 깔끔하게 숫자 정수만 추출해 냅니다.
DISK_USED=$(df --output=pcent / | tail -n 1 | tr -d ' %')


# ==================================================================
# STEP 5. 리눅스 표준 임계값 비교 및 실시간 경고 통보
# ==================================================================
# Bash의 정수 비교 연산자(-gt) 처리를 위해 소수점 뒷자리를 잘라내고 정수형 변수를 생성합니다.
# 예: 25.3% -> 25 (정수형 변환)
CPU_INT=${CPU%.*}
MEM_INT=${MEM%.*}

# 과제 필수 요구 스펙에 맞춰 각각의 자원이 한계선을 넘었는지 검사하여 경고를 띄웁니다.
if [ "$CPU_INT" -gt 20 ]; then
    echo "[WARNING] CPU threshold exceeded (${CPU}% > 20%)"
fi

if [ "$MEM_INT" -gt 10 ]; then
    echo "[WARNING] MEM threshold exceeded (${MEM}% > 10%)"
fi

if [ "$DISK_USED" -gt 80 ]; then
    echo "[WARNING] DISK threshold exceeded (${DISK_USED}% > 80%)"
fi


# ==================================================================
# STEP 6. 영구 로그 파일 저장 (Data Logging)
# ==================================================================
# 과제에서 제시한 대괄호 및 띄어쓰기 포맷을 한 자도 틀리지 않고 정확히 구현합니다.
# 기호 '>>'는 기존 로그 기록을 지우지 않고 맨 아래 줄에 계속해서 누적(Append)시키는 역할을 합니다.
echo "[$TIMESTAMP] PID:${PID} CPU:${CPU}% MEM:${MEM}% DISK_USED:${DISK_USED}%" >> "$LOG_FILE"


# ==================================================================
# STEP 7. 콘솔 디버깅용 실행 결과 출력 (UI Output)
# ==================================================================
# 엔지니어가 수동으로 이 스크립트를 실행했을 때 진행 상황을 한눈에 볼 수 있게 출력합니다.
echo "====== SYSTEM MONITOR RESULT ======"
echo "[HEALTH CHECK]"
echo "Checking process '$PROCESS_NAME'... [OK] (PID: $PID)"
echo "Checking port $PORT... [OK]"
echo "[RESOURCE MONITORING]"
echo "CPU Usage : ${CPU}%"
echo "MEM Usage : ${MEM}%"
echo "DISK Used : ${DISK_USED}%"
echo "[INFO] Log appended securely: $LOG_FILE"
